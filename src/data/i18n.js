export const getTranslationValue = (language, key, fallback = key) => {
  const dictionary = translations[language] || translations.ja;
  const fallbackDictionary = translations.en;
  const resolve = (source) =>
    key.split('.').reduce((value, part) => (value && value[part] !== undefined ? value[part] : undefined), source);

  const value = resolve(dictionary);
  if (value !== undefined) return value;

  const fallbackValue = resolve(fallbackDictionary);
  return fallbackValue !== undefined ? fallbackValue : fallback;
};

export const createTranslator = (language) => {
  const dictionary = translations[language] || translations.ja;
  const translate = (key, fallback) => getTranslationValue(language, key, fallback);

  return Object.assign(translate, dictionary);
};
